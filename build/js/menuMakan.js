document.addEventListener("DOMContentLoaded", function () {
  var hitungButton = document.getElementById("hitungButton");
  var jumlahDivHasil = 0;

  hitungButton.addEventListener("click", function () {
    if (dataBahanArray.length > 0) {
      var hasilDiv = document.getElementById("hasil");
      hasilDiv.style.display = "block";

      if (jumlahDivHasil !== dataBahanArray.length) {
        while (hasilDiv.firstChild) {
          hasilDiv.removeChild(hasilDiv.firstChild);
        }

        for (var i = 0; i < dataBahanArray.length; i++) {
          tambahDivHasil(dataBahanArray[i]);
        }

        jumlahDivHasil = dataBahanArray.length;
      }

      console.log(dataBahanArray);
    } else {
      alert("Please add at least one ingredient before calculating.");
    }
  });
});

